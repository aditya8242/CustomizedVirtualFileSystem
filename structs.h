/////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Structure Name:   BootBlock 
//    Description:      Holds the information for booting the operating system   
//
/////////////////////////////////////////////////////////////////////////////////////////////////

struct BootBlock
{
	char Information[100];
};

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Structure Name:   SuperBlock
//    Description:      Holds the information about the file system   
//
/////////////////////////////////////////////////////////////////////////////////////////////////

struct SuperBlock
{
	int TotalInodes;
	int FreeInodes;
};

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Structure Name:   Inode 
//    Description:      Holds the information about the file   
//
/////////////////////////////////////////////////////////////////////////////////////////////////

#pragma pack(1)
struct Inode
{
	char FileName[20];
	int InodeNumber;
	int FileSize;
	int FileType;
	int ActualFileSize;
	int ReferenceCount;
	int Permission;
	char *Buffer;
	struct Inode *next;
};

// typedef for Inode

typedef struct Inode INODE;
typedef struct Inode* PINODE;
typedef struct Inode** PPINODE;	// might not be used

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Structure Name:   FileTable 
//    Description:      Holds the information about opened file  
//
/////////////////////////////////////////////////////////////////////////////////////////////////

struct FileTable
{
	int ReadOffset;
	int WriteOffset;
	int Mode;
	PINODE ptrinode;
};

// typedef for FileTable

typedef FileTable FILETABLE;
typedef FileTable* PFILETABLE;

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Structure Name:   UAREA
//    Description:      Holds the information about process 
//
/////////////////////////////////////////////////////////////////////////////////////////////////

struct UAREA
{
	char ProcessName[20];
	PFILETABLE UFDT[MAXOPENFILES];
};